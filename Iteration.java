public class Iteration {
    public static void main(String[] args){
        //For Loop
        for(int i=0; i<=10; i++){
            System.out.println(i+" ");
        }

        //While Loop
        int i=0;
        while(i<=10){
            System.out.println(i);
            i++;
        }

        //Do While Loop
        int j=0;
        do{
            System.out.println(j);
            j++;
        }while(j<=10);
    }
}
